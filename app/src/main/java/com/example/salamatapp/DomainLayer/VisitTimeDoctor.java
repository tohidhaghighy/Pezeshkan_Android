package com.example.salamatapp.DomainLayer;

public class VisitTimeDoctor {
    private int Id;
    private String Date;
    private String Description;
    private String CountNobat;

    public int getId() {
        return Id;
    }

    public void setId(int id) {
        Id = id;
    }

    public String getDate() {
        return Date;
    }

    public void setDate(String date) {
        Date = date;
    }

    public String getDescription() {
        return Description;
    }

    public void setDescription(String description) {
        Description = description;
    }

    public String getCountNobat() {
        return CountNobat;
    }

    public void setCountNobat(String countNobat) {
        CountNobat = countNobat;
    }
}
